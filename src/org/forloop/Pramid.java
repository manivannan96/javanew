package org.forloop;

public class Pramid {
	public static void main(String [] args) {
		for (int i=1;i<=1;i++) {
			for(int j=1; j<=1;j++) {
				System.out.print("\t"+"\t"+"*");
			}
			System.out.println("\n");
		}
		for (int i=1;i<=1;i++) {
			for(int j=1; j<=2;j++) {
				System.out.print("  "+"\t"+"*");
				System.out.print("\t");
			}
			System.out.println("\n");
		}
		for (int i=1;i<=1;i++) {
			for(int j=1; j<=3;j++) {
				System.out.print("*");
				System.out.print("\t"+"\t");
			}
		}
	}

}
